    private boolean vTeclas(int key) {
        boolean validar = false;
        if (key == 32) {
            boolean espacio = true;
            validar = espacio;
        }
        if (key == 8) {
            boolean retroceso = true;
            validar = retroceso;
        }
        if (key == 11) {
            boolean tab = true;
            validar = tab;
        }
        if (key == 127) {
            boolean sup = true;
            validar = sup;
        }
        if (key == 10) {
            boolean enter = true;
            validar = enter;
        }
        return validar;
    }
