        boolean may = key >= 65 && key <= 90;
        boolean min = key >= 97 && key <= 122;
        boolean validar = vTeclas(key);

        if (!(may || min || validar)) {
            JOptionPane.showMessageDialog(this, "Este campo solo admite caracteres");
            evt.consume();
        }
