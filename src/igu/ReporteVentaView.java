    private void cargarVentas() {
        for (Venta aux : ventas) {
            jcVenta.addItem(aux);
        }
    }

    private void armarTabla() {
        ArrayList<Object> column = new ArrayList();
        column.add("Id Detalle");
        column.add("Cantidad");
        column.add("Precio");
        column.add("Producto");
        for (Object object : column) {
            modelo.addColumn(object);
        }
        jtVentas.setModel(modelo);
        jtVentas.getColumnModel().getColumn(0).setPreferredWidth(80);
        jtVentas.getColumnModel().getColumn(1).setPreferredWidth(80);
        jtVentas.getColumnModel().getColumn(2).setPreferredWidth(80);
        jtVentas.getColumnModel().getColumn(3).setPreferredWidth(360);
    }

    private void llenarTabla() {
        Venta seleccionada = (Venta) jcVenta.getSelectedItem();
        ArrayList<DetalleVenta> detalle = (ArrayList<DetalleVenta>) dVenta.listarDetallePorVenta(seleccionada.getIdVenta());
        for (DetalleVenta aux : detalle) {
            modelo.addRow(new Object[]{aux.getIdDetalleVent(), aux.getCantidad(), aux.getPrecioVenta(), aux.getIdProducto().getDescripcion()});
        }

    }

